CREATE TABLE CUSTOMERS1(
    C_ID VARCHAR(10) NOT NULL,
    USER_NAME VARCHAR(20),
    PASS VARCHAR(10),
    MOBILE_NO VARCHAR(15),
    EMAIL VARCHAR(40),
    COUNTRY VARCHAR(15),
    ADDRESS VARCHAR(100),
    PRIMARY KEY(C_ID)
);
CREATE TABLE PAYMENT(
    PAYMENT_ID VARCHAR(15),
    AMOUNT INT,
    PAYMENT_MODE VARCHAR(10),
    PAYMENT_TIME VARCHAR(10),
    PRIMARY KEY(PAYMENT_ID),
    ADMIN_ID VARCHAR(10),
    FOREIGN KEY (ADMIN_ID) REFERENCES ADMINISTRATION
);
CREATE TABLE MAKE_PAYMENT(
    C_ID VARCHAR(10),
    PAYMENT_ID VARCHAR(15),
    PRIMARY KEY (C_ID,PAYMENT_ID)
);
DROP TABLE MAKE_PAYMENT;
CREATE TABLE CART(
    CART_ID VARCHAR(10),
    NO_OF_PRODUCTS INT,
    TOTAL_PRICE INT,
    PRIMARY KEY(CART_ID),
    CUSTOMER_ID VARCHAR(10),
    REGISTRATION_ID VARCHAR(10),
    FOREIGN KEY (REGISTRATION_ID) REFERENCES REGISTRATION1,
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS1
);
CREATE TABLE ADMINISTRATION(
    ADMIN_ID VARCHAR(10) NOT NULL,
    PASSWORD VARCHAR(20),
    DETAILS VARCHAR(200),
    PRIMARY KEY(ADMIN_ID)
);
CREATE TABLE CAMERA(
    CAMERA_ID VARCHAR(10) NOT NULL,
    MACROLENS INTEGER,
    MAINCAMERA INTEGER,
    ULTRAWIDE INTEGER,
    IOS VARCHAR(3),
    SENSOR_NO VARCHAR(10),
    PRIMARY KEY(CAMERA_ID)
);
CREATE TABLE REGISTRATION1(
REGISTRATION_ID VARCHAR(10),
MODEL_NO VARCHAR(10),
TOTAL_PRICE INT,
CUSTOMER_ID VARCHAR(10),
FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS1,
FOREIGN KEY (MODEL_NO) REFERENCES PHONE,
PRIMARY KEY (REGISTRATION_ID)
);

CREATE TABLE PROCESSOR(
    PROCESSOR_ID VARCHAR(10) NOT NULL,
    PROCESSOR_TYPE VARCHAR(10),
    PRIMARY_CLOCK_SPEED INT,
    SEC_CLOCK_SPEED INT,
    NO_OF_CORE INT,
    NO_OF_THREADS INT,
    PRIMARY KEY (PROCESSOR_ID)
);
CREATE TABLE PHONE(
    MODEL_NO VARCHAR(15) NOT NULL,
    RAM INT,
    ROM INT,
    CONECTIVITY VARCHAR(10),
    BATTERY INT,
    PRICE INT,
    PROCESSOR_ID VARCHAR(20),
    CAMERA_ID VARCHAR(20),
    ADMIN_ID VARCHAR(10),
    PRIMARY KEY(MODEL_NO),
    TYPE VARCHAR(4),
    FOREIGN KEY(PROCESSOR_ID) REFERENCES PROCESSOR,
    FOREIGN KEY(CAMERA_ID) REFERENCES CAMERA,
    FOREIGN KEY(ADMIN_ID) REFERENCES ADMINISTRATION
);










